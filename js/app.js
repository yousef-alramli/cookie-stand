// this is from w3cschools
function random(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;

};

let hourslist = ['6am', '7am', '8am', '9am', '10am', '11am', '12am', '1pm', '2pm', '3pm', '4pm', '5pm', '6pm', '7pm']

let listofbranches = [];



function Salmoncookie(name, min, max, avgcookie,) {
    this.name = name;
    this.min = min;
    this.max = max;
    this.avgcookie = avgcookie;

    listofbranches.push(this);
    this.randomcustomer = [];
    this.randomcookie = [];

};




let seattle = new Salmoncookie('seattle', 23, 65, 6.3)
let tokyo = new Salmoncookie('tokyo', 3, 24, 1.2)
let dubai = new Salmoncookie('dubai', 11, 38, 3.7)
let paris = new Salmoncookie('paris', 20, 38, 2.3)
let lima = new Salmoncookie('lima', 2, 16, 4.6)





let parent = document.getElementById('salmon');
let divelement = document.createElement('div');
let table = document.createElement('table');

divelement.appendChild(table);
parent.appendChild(divelement);

let trhead = document.createElement('tr')
table.appendChild(trhead)
let thhead = document.createElement('th');
trhead.appendChild(thhead);
thhead.textContent = ''


function header() {

    for (let i = 0; i < hourslist.length; i++) {
        let thhead = document.createElement('th');
        trhead.appendChild(thhead);
        thhead.textContent = hourslist[i]

    }

    let thhead = document.createElement('th');
    trhead.appendChild(thhead);
    thhead.textContent = 'Daily Location Total'
}
header();



let trbody = document.createElement('tr')
table.appendChild(trbody)

let listoflocationtotal= [];

Salmoncookie.prototype.render = function () {

    for (let i = 0; i < hourslist.length; i++) {
        this.randomcustomer.push(random(this.min, this.max));

        this.randomcookie.push(Math.round(this.avgcookie * this.randomcustomer[i]));
    }
    ;


    let trbody = document.createElement('tr')
    table.appendChild(trbody)
    let thbody = document.createElement('th')
    trbody.appendChild(thbody);
    thbody.textContent = this.name;

    for (let x = 0; x < this.randomcookie.length; x++) {

        let thbody = document.createElement('th');
        trbody.appendChild(thbody);
        thbody.textContent = this.randomcookie[x];


    }

    let locationtotal = 0;
    for (let j = 0; j < this.randomcookie.length; j++) {

        locationtotal += this.randomcookie[j]
        listoflocationtotal.push(locationtotal)

    }
    

    thbody = document.createElement('th');
    trbody.appendChild(thbody);
    thbody.textContent = locationtotal

}

for (let y = 0; y < listofbranches.length; y++) {

    listofbranches[y].render();

}


let totalcolumn = [];
let hourlytotal = 0;

for (let j = 0; j < hourslist.length; j++) {

    hourlytotal = 0

    for (var i = 0; i < listofbranches.length; i++) {
        hourlytotal += listofbranches[i].randomcookie[j];

    }

    totalcolumn.push(hourlytotal)

}



function footer() {

    let trfooter = document.createElement('tr')
    table.appendChild(trfooter)
    let thfooter = document.createElement('th');
    trfooter.appendChild(thfooter);
    thfooter.textContent = 'total'
    for (let i = 0; i < hourslist.length; i++) {
        thfooter = document.createElement('th');
        trfooter.appendChild(thfooter);
        thfooter.textContent = totalcolumn[i];

    }

    let totaloftotal = 0;
    for (let j = 0; j < listofbranches.length; j++) {
        totaloftotal += listoflocationtotal[j];
    }

    thfooter = document.createElement('th');
    trfooter.appendChild(thfooter);
    thfooter.textContent = totaloftotal

}

footer();

